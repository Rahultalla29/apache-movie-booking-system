    private void applyFilterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_applyFilterActionPerformed
        String loc = (String)locationf.getSelectedItem();
        String session = (String)timef.getSelectedItem();
        String showing = (String)showingWeekf.getSelectedItem();
        String type = (String)screenTypef.getSelectedItem();
        String moviesWanted = movieWantedf.getText();
        ArrayList<Showing> s = App.getAppInstance().db.getShows();
//        System.out.println(s.size());
        // i1 = We cant get all rows to display => Length
        // More than 6 rows and program fails
        
        if (loc.equals("All") && session.equals("All") && showing.equals("Movies This Week") &&  type.equals("All") && moviesWanted.equals("")){
            System.out.println("No Filters Applied!");
            s = App.getAppInstance().db.showingsTillNextMonday(s);
            allMovies.setModel(App.getAppInstance().db.getJtableSForDisplay(s).getModel());
        }
        else {
                System.out.println("Filters Applied");
                
                if (!loc.equals("All")){
                    s = App.getAppInstance().db.showingsByLocation(s, loc);
                }
                if (!session.equals("All")){
                    s = App.getAppInstance().db.showingsBySession(s, session);
                }
                if (showing.equals("Movies Today")){
                    s = App.getAppInstance().db.showingsByToday(s);
                }
                if (!type.equals("All")){
                    s = App.getAppInstance().db.showingsByScreenSize(s, type);
                }
                if (!moviesWanted.equals("")){
                    s = App.getAppInstance().db.showingsByTitle(s, moviesWanted);
                }
                
                allMovies.setModel(App.getAppInstance().db.getJtableSForDisplay(s).getModel());
                
                s  = App.getAppInstance().db.getShows();
        }
    }//GEN-LAST:event_applyFilterActionPerformed

