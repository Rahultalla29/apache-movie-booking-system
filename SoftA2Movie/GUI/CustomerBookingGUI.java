    public int IsEmpty(String str) {
        int val = 0;
        if (str == null ||str.equals("")){
            return val;
        }else{
            val = Integer.parseInt(str);
            if (val < 0){
                val = val*-1;
            }
        }
        return val;
    }
    public String IsEmpty(String str,String replaceWith) {
        
        if (str == null ||str.equals("")){
            return null;
        }
        return str;
    }
    
